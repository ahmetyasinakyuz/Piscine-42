int	ft_is_prime(int nb)
{
	int	i;
	int	a;

	a = 0;
	i = 2;
	if (nb <= 1)
		return (0);
	while (i < nb)
	{
		a = nb % i;
		if (a == 0)
		{
			return (0);
		}
		i++;
	}
	return (1);
}
