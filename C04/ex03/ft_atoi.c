int	ft_atoi(char *str)
{
	int	a;
	int	b;
	int	result;

	result = 0;
	a = 0;
	b = 1;
	while ((str[a] == ' ')
		|| (str[a] >= '\t' && str[a] <= '\r'))
		a++;
	while (str[a] == '-' || str[a] == '+')
	{
		if (str[a] == '-')
		{
			b = b * -1;
		}
		a++;
	}
	while (str[a] >= '0' && str[a] <= '9')
	{
		result = result * 10 + (str[a] - '0');
		a++;
	}
	return (result * b);
}
