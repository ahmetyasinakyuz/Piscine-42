char	*ft_strcat(char *dest, char *src)
{
	int	i;
	int	a;

	a = 0;
	i = 0;
	while (dest[a])
	{
		a++;
	}
	while (src[i])
	{
		dest[a] = src[i];
		i++;
		a++;
	}
	dest[a] = '\0';
	return (dest);
}
